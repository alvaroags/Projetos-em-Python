num = int(input())
if((num<1) or (num>7)):
    print('DIA DA SEMANA INVÁLIDO')
else:
    if(num==1):
        print('SEGUNDA-FEIRA')
    elif(num==2):
        print('TERÇA-FEIRA')
    elif(num==3):
        print('QUARTA-FEIRA')
    elif(num==4):
        print('QUINTA-FEIRA')
    elif(num==5):
        print('SEXTA-FEIRA')
    elif(num==6):
        print('SABADO')
    elif(num==7):
        print('DOMINGO')